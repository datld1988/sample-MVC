package com.vtc.model;

public class PackageInfo {
	private String name;
	private String priceGold;
	private String priceVcoin;
	private String bonusPercent;
	private String image;
	private String description;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPriceGold() {
		return priceGold;
	}
	public void setPriceGold(String priceGold) {
		this.priceGold = priceGold;
	}
	public String getPriceVcoin() {
		return priceVcoin;
	}
	public void setPriceVcoin(String priceVcoin) {
		this.priceVcoin = priceVcoin;
	}
	public String getBonusPercent() {
		return bonusPercent;
	}
	public void setBonusPercent(String bonusPercent) {
		this.bonusPercent = bonusPercent;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
}
